let numero1 = parseInt(prompt("Ingrese un numero entero"))
let numero2 = parseInt(prompt("Ingrese el otro numero entero"))

if ((numero1 >= 100) && (numero2<= 500)){
    alert("Los numeros ingresados estan entre 100 y 500 y son: "+numero1+" y "+numero2)
}else{
    alert("Los numeros ingresados no estan entre 100 y 500 y son: " +numero1+ " y "+numero2)
}
